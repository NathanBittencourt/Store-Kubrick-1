  <p class="h3 my-5 text-center">
    A história do cinema começa no final do século XIX, quando os irmãos Lumière apresentaram,
    em dezembro de 1895, sua primeira exibição pública de um filme em movimento,
    intitulado "A Chegada do Trem à Estação". Esse momento foi considerado o nascimento da sétima arte.
  </p>
  <div class="text-center">
    <img src="<?= baseUrl() ?>/assets/gifs/lumiereTremEstacao.gif" alt="lumiereTremEstacao" class=" w-100 rounded">
  </div>

  <p class="h3 my-5 text-center">
    No início do cinema, as produções eram mudas e em preto e branco,
    e a narrativa era muitas vezes baseada em histórias simples, como perseguições,
    comédias e melodramas. No entanto, ao longo do tempo, o cinema se desenvolveu em um meio complexo,
    com narrativas sofisticadas,efeitos especiais e um grande impacto cultural.
  </p>
  <div class="text-center">
    <img src="<?= baseUrl() ?>/assets/gifs/chaplin.gif" alt="chaplin" class=" w-100 rounded">
  </div>

  <p class="h3 my-5 text-center">
    Durante a primeira metade do século XX, Hollywood, nos Estados Unidos, se tornou o centro mundial da produção cinematográfica,
    com estúdios gigantes como a Warner Bros., Paramount, MGM e Universal produzindo centenas de filmes por ano. Grandes diretores,
    como Alfred Hitchcock, Orson Welles e Charlie Chaplin, ajudaram a moldar o cinema como o conhecemos hoje, criando obras-primas como "Psicose",
    "Cidadão Kane" e "Tempos Modernos".
  </p>
  <div class="text-center">
    <img src="<?= baseUrl() ?>/assets/gifs/psicose.gif" alt="psicose" class=" w-100 rounded">
  </div>

  <p class="h3 my-5 text-center">
    Na década de 60, um jovem diretor chamado Stanley Kubrick desafiou as convenções do cinema clássico de Hollywood com filmes como
    "Dr. Fantástico" e "2001: Uma Odisseia no Espaço", que exploravam novas formas de narrativa e estética. Kubrick, considerado um dos maiores
    cineastas de todos os tempos, deixou um legado duradouro na história do cinema.
  </p>
  <div class="text-center">
    <img src="<?= baseUrl() ?>/assets/gifs/2001.gif" alt="2001" class=" w-100 rounded">
  </div>

  <p class="h3 my-5 text-center">
    Nos anos 70, o cinema foi influenciado pela contracultura e pelos movimentos sociais,
    resultando em uma série de filmes políticos e de vanguarda, como "Taxi Driver" e "Apocalypse Now".
    Na década de 80, o cinema se tornou cada vez mais comercial, com o surgimento de blockbusters como "Star Wars" e "Indiana Jones".
  </p>
  <div class="text-center">
    <img src="<?= baseUrl() ?>/assets/gifs/star-wars.gif" alt="star-wars" class=" w-100 rounded">
  </div>

  <p class="h3 my-5 text-center">
    Hoje, o cinema continua a evoluir, com novas tecnologias e plataformas de distribuição transformando a maneira
    como os filmes são feitos e vistos. No entanto, mesmo com todas essas mudanças, a magia do cinema permanece,
    fascinando espectadores de todo o mundo e proporcionando uma experiência única e emocionante.
  </p>
